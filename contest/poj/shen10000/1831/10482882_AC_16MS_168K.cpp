int ans[100];

void print_smal(int* p){
	printf("%d",p[0]);
	for(int i=1;i<=p[0];i++)
		printf(" %d",p[i]);
	printf("\n");
}

void dfs(int sum,int id,int pre){
	if(sum>=57){
		if(sum&1){
			dfs((sum-9)>>1,id+2,pre<<1);
			ans[id]=3*pre;
			ans[id+1]=ans[id]<<1;
		}else{
			dfs((sum-2)>>1,id+1,pre<<1);
			ans[id]=pre<<1;
		}
	}else{
		for(int i=1;i<=str[sum][0];i++){
			ans[id++]=str[sum][i]*pre;
		}
		ans[0]=id-1;
	}
}

